#include <iostream>
using namespace std;
#include <cmath>

int main() {
  cout << max(5, 10);
  cout << min(5, 10);
  cout << "Square root of 64 is: " << sqrt(64) << "\n";
  cout << "Round 2.6 to the nearest integer: " << round(2.6) << "\n";
  cout << "The natural logarithm of 2 is: " <<log(2) << "\n";
  return 0;
}
