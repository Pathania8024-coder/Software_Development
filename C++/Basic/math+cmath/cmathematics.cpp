#include <iostream>
#include <cmath>
using namespace std;

int main() {
              // absolute
  cout << abs(-4.7) << "\n";
  cout << abs(4.7) << "\n"; 
  cout << abs(3) << "\n";
//   acos
  cout << acos(0.64) << "\n";
  cout << acos(-0.4) << "\n";
  cout << acos(0) << "\n";
  cout << acos(1) << "\n";
  cout << acos(-1) << "\n";
  cout << acos(2) << "\n";
//   acosh
  cout << acosh(7) << "\n";
  cout << acosh(56) << "\n";
  cout << acosh(2.45) << "\n";
//   asin
  cout << asin(0.64) << "\n";
  cout << asin(-0.4) << "\n";
  cout << asin(0) << "\n";
  cout << asin(1) << "\n";
  cout << asin(-1) << "\n";
  cout << asin(2) << "\n";

  return 0;
}
