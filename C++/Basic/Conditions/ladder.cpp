using namespace std;

int main(){

              int day = 5;

              if (day==1){
                            /* code */
                            cout<<"monday";
              }else if (day==2){
                            /* code */
                            cout<<"tuesday";
              }else if (day==3){
                            /* code */
                            cout<<"wednesday";
              }else if (day==4){
                            /* code */
                            cout<<"thursday";
              }else if (day==5){
                            /* code */
                            cout<<"friday";
              }else if (day==6){
                            /* code */
                            cout<<"saturday";
              }else if (day==7){
                            /* code */
                            cout<<"sunday";
              }else{
                            /* code */
                            cout<<"invalid input";
              }

              return 0;
              
}